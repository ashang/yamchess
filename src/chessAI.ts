// Get position table for a piece type
function getPositionTable(piece: string): number[][] {
  const type = piece.toLowerCase();
  switch (type) {
    case 'p': return PAWN_TABLE;
    case 'n': return KNIGHT_TABLE;
    case 'b': return BISHOP_TABLE;
    case 'r': return ROOK_TABLE;
    case 'q': return QUEEN_TABLE;
    case 'k': return KING_TABLE;
    default: return [];
  }
}

// Evaluate a chess position
function evaluateBoard(chess: Chess): number {
  let score = 0;
  const board = chess.board();

  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (piece) {
        const pieceType = piece.type;
        const color = piece.color;
        const value = PIECE_VALUES[pieceType];
        const table = getPositionTable(pieceType);
        
        // Get position bonus (flip table for black pieces)
        let positionBonus = 0;
        if (table.length > 0) {
          const tableRow = color === 'w' ? row : 7 - row;
          positionBonus = table[tableRow][col];
        }

        // Add to score (positive for white, negative for black)
        if (color === 'w') {
          score += value + positionBonus;
        } else {
          score -= value + positionBonus;
        }
      }
    }
  }

  return score;
}

// Minimax algorithm with alpha-beta pruning
function minimax(
  chess: Chess,
  depth: number,
  alpha: number,
  beta: number,
  isMaximizing: boolean
): number {
  if (depth === 0 || chess.isGameOver()) {
    return evaluateBoard(chess);
  }

  const moves = chess.moves();

  if (isMaximizing) {
    let maxEval = -Infinity;
    for (const move of moves) {
      chess.move(move);
      const evalScore = minimax(chess, depth - 1, alpha, beta, false);
      chess.undo();
      maxEval = Math.max(maxEval, evalScore);
      alpha = Math.max(alpha, evalScore);
      if (beta <= alpha) break;
    }
    return maxEval;
  } else {
    let minEval = Infinity;
    for (const move of moves) {
      chess.move(move);
      const evalScore = minimax(chess, depth - 1, alpha, beta, true);
      chess.undo();
      minEval = Math.min(minEval, evalScore);
      beta = Math.min(beta, evalScore);
      if (beta <= alpha) break;
    }
    return minEval;
  }
}

// Find the best move using minimax
export function findBestMove(chess: Chess, depth: number = 3): string | null {
  const moves = chess.moves();
  if (moves.length === 0) return null;

  let bestMove = null;
  let bestValue = -Infinity;
  const isWhite = chess.turn() === 'w';

  for (const move of moves) {
    chess.move(move);
    const boardValue = minimax(chess, depth - 1, -Infinity, Infinity, !isWhite);
    chess.undo();

    // For black, we want to minimize the value
    const moveValue = isWhite ? boardValue : -boardValue;
    
    if (moveValue > bestValue) {
      bestValue = moveValue;
      bestMove = move;
    }
  }

  return bestMove;
}
